using UnityEngine;

public class RecipeManager : MonoBehaviour
{
    //도마 매니저 (재료통 버튼 클릭시) / (재료 불 변수 On)  / 열등, 무기력, 좌절, 공포, 분노, 불안
    /*
        자신감을 주는 과일케이크 (공포)
        희망을 주는 치즈식빵 (무기력)
        용기를 복돋아주는 피자빵 (좌절)
        자존감의 딸기 도넛 (열등)
        안정의 도넛.(불안)
        진정의 거미머핀(분노)

        
        거미치즈 + 산딸기 = 피자빵
        거미치즈 + 개구리알 = 도넛
        개구리알 + 산딸기 = 과일케이크 
        거미치즈 = 치즈식빵
        산딸기 = 딸기 도넛
        개구리알 = 개구리알 머핀
     */

    public bool SpiderCheese = false;
    public bool FrogEye = false;
    public bool StarwBerry = false;
    public bool BDoughnut = false;
    public bool PB = false;
    public bool FC = false;
    public bool CB = false;
    public bool SBD = false;
    public bool SM = false;

    public GameObject Dough;
    public GameObject Spider;
    public GameObject Frog;
    public GameObject Berry;
    public GameObject Doughnut;
    public GameObject PizzaBread;
    public GameObject FruitCake;
    public GameObject CheeseBread;
    public GameObject SBDoughnut;
    public GameObject SpiderMuffin;

    public void SC()
    {
        if (Spider.activeSelf)
        {
            Debug.Log("이미 재료로 사용중입니다.");
            return;
        }
        if (Dough.activeSelf)
        {
            Debug.Log("이미 반죽이 완성 되었습니다.");
            return;
        }
        if (Doughnut.activeSelf || PizzaBread.activeSelf || FruitCake.activeSelf || CheeseBread.activeSelf || SBDoughnut.activeSelf || SpiderMuffin.activeSelf)
        {
            Debug.Log("이미 빵을 구우셨습니다.");
            return;
        }
        SpiderCheese = true;
        Debug.Log("거미치즈를 재료로 사용했습니다.");
        Spider.SetActive(true);
    }


    public void FE()
    {

        if (Frog.activeSelf)
        {
            Debug.Log("이미 재료로 사용중입니다.");
            return;
        }
        if (Dough.activeSelf)
        {
            Debug.Log("이미 반죽이 완성 되었습니다.");
            return;
        }
        if (Doughnut.activeSelf || PizzaBread.activeSelf || FruitCake.activeSelf || CheeseBread.activeSelf || SBDoughnut.activeSelf || SpiderMuffin.activeSelf)
        {
            Debug.Log("이미 빵을 구우셨습니다.");
            return;
        }
        FrogEye = true;
        Debug.Log("개구리눈을 재료로 사용했습니다.");
        Frog.SetActive(true);
    }

    public void SB()
    {
        if (Berry.activeSelf)
        {
            Debug.Log("이미 재료로 사용중입니다.");
            return;
        }
        if (Dough.activeSelf)
        {
            Debug.Log("이미 반죽이 완성 되었습니다.");
            return;
        }
        if (Doughnut.activeSelf || PizzaBread.activeSelf || FruitCake.activeSelf || CheeseBread.activeSelf || SBDoughnut.activeSelf || SpiderMuffin.activeSelf)
        {
            Debug.Log("이미 빵을 구우셨습니다.");
            return;
        }

        StarwBerry = true;
        Debug.Log("엘프숲의 딸기를 재료로 사용했습니다.");
        Berry.SetActive(true);
    }

    public void Synthesis()
    {
        if (SpiderCheese == true && FrogEye == true && StarwBerry == false)
        {
            Spider.SetActive(false);
            Berry.SetActive(false);
            Frog.SetActive(false);
            Dough.SetActive(true);



            Debug.Log("안정의 도넛반죽을 만들었습니다.");
        }
        else if (SpiderCheese == false && FrogEye == true && StarwBerry == true)
        {
            Spider.SetActive(false);
            Berry.SetActive(false);
            Frog.SetActive(false);
            Dough.SetActive(true);


            Debug.Log("자신감을 주는 과일케이크 반죽을 만들었습니다.");
        }
        else if (SpiderCheese == true && FrogEye == false && StarwBerry == true)
        {
            Spider.SetActive(false);
            Berry.SetActive(false);
            Frog.SetActive(false);
            Dough.SetActive(true);


            Debug.Log("용기를 복돋아주는 피자빵 반죽을 만들었습니다.");
        }
        else if (SpiderCheese == true && FrogEye == false && StarwBerry == false)
        {
            Spider.SetActive(false);
            Frog.SetActive(false);
            Berry.SetActive(false);
            Dough.SetActive(true);
            Debug.Log("희망을 주는 치즈식빵반죽을 만들었습니다.");
        }
        else if (SpiderCheese == false && FrogEye == true && StarwBerry == false)
        {
            Spider.SetActive(false);
            Frog.SetActive(false);
            Berry.SetActive(false);
            Dough.SetActive(true);
            Debug.Log("진정의 거미머핀반죽을 만들었습니다.");
        }
        else if (SpiderCheese == false && FrogEye == false && StarwBerry == true)
        {
            Spider.SetActive(false);
            Frog.SetActive(false);
            Berry.SetActive(false);
            Dough.SetActive(true);
            Debug.Log("자존감의 딸기 도넛반죽을 만들었습니다.");
        }


    }

    public void Reset()
    {

        FrogEye = false;
        SpiderCheese = false;
        StarwBerry = false;
        BDoughnut = false;
        PB = false;
        FC = false;
        CB = false;
        SBD = false;
        SM = false;


        Spider.SetActive(false);
        Frog.SetActive(false);
        Berry.SetActive(false);
        Dough.SetActive(false);
        Doughnut.SetActive(false);
        PizzaBread.SetActive(false);
        FruitCake.SetActive(false);
        CheeseBread.SetActive(false);
        SBDoughnut.SetActive(false);
        SpiderMuffin.SetActive(false);
        Debug.Log("도마의 정 상 화");
    }

    public void Bake()
    {
        if (SpiderCheese == true && FrogEye == true && StarwBerry == false)
        {
            Spider.SetActive(false);
            Berry.SetActive(false);
            Frog.SetActive(false);
            Dough.SetActive(false);

            Doughnut.SetActive(true);

            BDoughnut = true;
            FrogEye = false;
            SpiderCheese = false;
            StarwBerry = false;
            PB = false;
            FC = false;
            CB = false;
            SBD = false;
            SM = false;

            Debug.Log("안정의 도넛이 구워졌습니다.");
        }
        else if (SpiderCheese == true && FrogEye == false && StarwBerry == false)
        {
            Spider.SetActive(false);
            Berry.SetActive(false);
            Frog.SetActive(false);
            Dough.SetActive(false);

            CheeseBread.SetActive(true);

            CB = true;
            FrogEye = false;
            SpiderCheese = false;
            StarwBerry = false;
            BDoughnut = false;
            PB = false;
            FC = false;
            SBD = false;
            SM = false;

            Debug.Log("희망을 주는 치즈식빵이 구워졌습니다.");
        }
        else if (SpiderCheese == true && FrogEye == false && StarwBerry == true)
        {
            Spider.SetActive(false);
            Berry.SetActive(false);
            Frog.SetActive(false);
            Dough.SetActive(false);

            PizzaBread.SetActive(true);

            PB = true;
            FrogEye = false;
            SpiderCheese = false;
            StarwBerry = false;
            BDoughnut = false;
            FC = false;
            CB = false;
            SBD = false;
            SM = false;

            Debug.Log("용기를 복돋아주는 피자빵이 구워졌습니다.");
        }
        else if (SpiderCheese == false && FrogEye == false && StarwBerry == true)
        {
            Spider.SetActive(false);
            Berry.SetActive(false);
            Frog.SetActive(false);
            Dough.SetActive(false);

            SBDoughnut.SetActive(true);

            SBD = true;
            FrogEye = false;
            SpiderCheese = false;
            StarwBerry = false;
            BDoughnut = false;
            PB = false;
            FC = false;
            CB = false;
            SM = false;

            Debug.Log("자존감의 딸기 도넛이 구워졌습니다.");
        }
        else if (SpiderCheese == false && FrogEye == true && StarwBerry == true)
        {
            Spider.SetActive(false);
            Berry.SetActive(false);
            Frog.SetActive(false);
            Dough.SetActive(false);

            FruitCake.SetActive(true);

            FC = true;
            FrogEye = false;
            SpiderCheese = false;
            StarwBerry = false;
            BDoughnut = false;
            PB = false;
            CB = false;
            SBD = false;
            SM = false;

            Debug.Log("자신감을 주는 과일케이크가 구워졌습니다.");
        }
        else if (SpiderCheese == false && FrogEye == true && StarwBerry == false)
        {
            Spider.SetActive(false);
            Berry.SetActive(false);
            Frog.SetActive(false);
            Dough.SetActive(false);

            SpiderMuffin.SetActive(true);

            SM = true;
            FrogEye = false;
            SpiderCheese = false;
            StarwBerry = false;
            BDoughnut = false;
            PB = false;
            FC = false;
            CB = false;
            SBD = false;

            Debug.Log("진정의 거미머핀이 구워졌습니다.");
        }

    }

}